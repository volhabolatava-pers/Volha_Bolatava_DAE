
CREATE SCHEMA IF NOT EXISTS BL_DM;

CREATE SEQUENCE IF NOT EXISTS BL_DM.SEQ_DIM_STORES       START 1;
CREATE SEQUENCE IF NOT EXISTS BL_DM.SEQ_DIM_VENDORS      START 1;
CREATE SEQUENCE IF NOT EXISTS BL_DM.SEQ_DIM_ITEMS_SCD    START 1;
CREATE SEQUENCE IF NOT EXISTS BL_DM.SEQ_DIM_PROMOTIONS   START 1;

COMMIT;

CREATE TABLE IF NOT EXISTS BL_DM.DIM_DATES_DAY (
  date_id      BIGINT NOT NULL PRIMARY KEY,  
  date_dt      date NOT NULL,
  day         integer NOT NULL,
  day_of_week VARCHAR(10) NOT NULL,
  is_weekend  boolean NOT NULL,
  month        integer NOT NULL,
  quarter      integer NOT NULL,
  year         integer NOT NULL);

CREATE TABLE IF NOT EXISTS BL_DM.DIM_STORES (
  STORE_SURR_ID     BIGINT NOT NULL PRIMARY KEY,
  STORE_SRC_ID      varchar(100) NOT NULL,
  STORE_NAME        varchar(100) NOT NULL,
  STORE_TYPE        varchar(10) NOT NULL,
  HOUSE_NUMBER      integer NOT NULL,
  CITY_NAME         varchar(20) NOT NULL,
  ZIP_CODE          varchar(5) NOT NULL,
  COUNTY_NAME       varchar(20) NOT NULL,
  TA_INSERT_DT      date NOT NULL,
  TA_UPDATE_DT      date NOT NULL,
  TA_SOURCE_SYSTEM  VARCHAR(50) NOT NULL,
  TA_SOURCE_ENTITY  VARCHAR(50) NOT NULL);

CREATE TABLE IF NOT EXISTS BL_DM.DIM_ITEMS_SCD (
  ITEM_SURR_ID          BIGINT    PRIMARY KEY    NOT NULL,
  ITEM_SRC_ID           VARCHAR(100)  NOT NULL,
  ITEM_NAME             VARCHAR(100)  NOT NULL,
  PACK                  INTEGER       NOT NULL,
  BOTTLE_VOLUME_ML      INTEGER       NOT NULL,
  STATE_BOTTLE_RETAIL   DECIMAL(10,2)  NOT NULL,
  STATE_BOTTLE_COST     DECIMAL(10,2)  NOT NULL,
  ITEM_CATEGORY_ID      BIGINT        NOT NULL,
  ITEM_CATEGORY_NAME    VARCHAR(50)   NOT NULL,
  TA_START_DT           DATE          NOT NULL,
  TA_END_DT             DATE          NOT NULL,
  TA_IS_ACTIVE          VARCHAR(1)    NOT NULL,
  TA_INSERT_DT          DATE          NOT NULL,
  TA_SOURCE_SYSTEM      VARCHAR(50)   NOT NULL,
  TA_SOURCE_ENTITY      VARCHAR(50)   NOT NULL);

CREATE TABLE IF NOT EXISTS BL_DM.DIM_VENDORS (
  VENDOR_SURR_ID         BIGINT NOT NULL PRIMARY KEY, 
  VENDOR_SRC_ID     VARCHAR(10) NOT NULL,
  VENDOR_NAME       varchar(100) NOT NULL,
  TA_INSERT_DT      date NOT NULL,
  TA_UPDATE_DT      date NOT NULL,
  TA_SOURCE_SYSTEM  VARCHAR(50) NOT NULL,
  TA_SOURCE_ENTITY  VARCHAR(50) NOT NULL);


CREATE TABLE IF NOT EXISTS BL_DM.DIM_PROMOTIONS (
  PROMO_SURR_ID          BIGINT NOT NULL PRIMARY KEY, 
  PROMO_SRC_ID      VARCHAR(10) NOT NULL,
  PROMO_NAME        varchar(20) NOT NULL,
  DISCOUNT_PCT      integer,
  TA_INSERT_DT      date NOT NULL,
  TA_UPDATE_DT      date NOT NULL,
  TA_SOURCE_SYSTEM  VARCHAR(50) NOT NULL,
  TA_SOURCE_ENTITY  VARCHAR(50) NOT NULL);


CREATE TABLE IF NOT EXISTS BL_DM.FCT_SALES_DD (
  EVENT_DT            DATE NOT NULL,
  DATE_ID             BIGINT NOT NULL,
  STORE_SURR_ID       BIGINT NOT NULL,
  VENDOR_SURR_ID      BIGINT NOT NULL,
  ITEM_SURR_ID        BIGINT NOT NULL, 
  PROMO_SURR_ID       BIGINT NOT NULL,
  FCT_BOTTLES_SOLD_QTY        integer,
  FCT_SALE_AMT_USD        decimal(15,3),
  FCT_VOLUME_SOLD_LTR  decimal(12,3),
  FCT_PROFIT_AMT_USD     decimal(15,3),
  TA_INSERT_DT      date NOT NULL,
  CONSTRAINT FK_FCT_SALES_DD_DIM_DATES FOREIGN KEY(DATE_ID)
  REFERENCES BL_DM.DIM_DATES_DAY(DATE_ID),
  CONSTRAINT FK_FCT_SALES_DD_STORES FOREIGN KEY(STORE_SURR_ID)
  REFERENCES BL_DM.DIM_STORES(STORE_SURR_ID),
  CONSTRAINT FK_FCT_SALES_DD_DIM_VENDORS FOREIGN KEY(VENDOR_SURR_ID)
  REFERENCES BL_DM.DIM_VENDORS(VENDOR_SURR_ID),
  CONSTRAINT FK_FCT_SALES_DD_DIM_ITEMS_SCD FOREIGN KEY(ITEM_SURR_ID)
  REFERENCES BL_DM.DIM_ITEMS_SCD (ITEM_SURR_ID),
  CONSTRAINT FK_FCT_SALES_DD_DIM_PROMOTIONS FOREIGN KEY(PROMO_SURR_ID)
  REFERENCES BL_DM.DIM_PROMOTIONS(PROMO_SURR_ID));


COMMIT;


