UPDATE BL_DM.DIM_ITEMS_SCD scd
SET TA_END_DT = i.TA_START_DT - INTERVAL '1 day',
TA_IS_ACTIVE = 'N',
TA_INSERT_DT = CURRENT_DATE
FROM BL_3NF.CE_ITEMS_SCD AS i 
LEFT JOIN BL_3NF.CE_CATEGORIES cat ON i.CATEGORY_ID = cat.CATEGORY_ID
WHERE scd.ITEM_SRC_ID = i.ITEM_ID::VARCHAR
  AND scd.TA_IS_ACTIVE = 'Y'
  AND i.TA_IS_ACTIVE = 'Y'
  AND (scd.ITEM_NAME          IS DISTINCT FROM COALESCE(i.ITEM_NAME, 'n.a.') OR
      scd.PACK                IS DISTINCT FROM i.PACK OR
      scd.BOTTLE_VOLUME_ML    IS DISTINCT FROM i.BOTTLE_VOLUME_ML OR
      scd.STATE_BOTTLE_RETAIL IS DISTINCT FROM i.STATE_BOTTLE_RETAIL OR
      scd.STATE_BOTTLE_COST   IS DISTINCT FROM i.STATE_BOTTLE_COST OR
      scd.ITEM_CATEGORY_NAME  IS DISTINCT FROM COALESCE(cat.CATEGORY_NAME, 'n.a.'));
    

