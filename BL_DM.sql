INSERT INTO BL_DM.DIM_DATES_DAY (date_id, date_dt, day, day_of_week,is_weekend, month,quarter,year)
SELECT 
to_char (datum, 'YYYYMMDD'):: bigint,
datum::date,
extract(day from datum),
to_char(datum, 'TMDay'),
CASE 
        WHEN extract(isodow from datum) IN (6, 7) THEN TRUE 
        ELSE FALSE
END,
extract(month from datum),
    extract(quarter from datum),
    extract(year from datum)
FROM 
	generate_series('2024-01-01'::date, 
        '2025-12-31'::date, 
        '1 day'::interval) AS datum;


INSERT INTO BL_DM.DIM_STORES(STORE_SURR_ID, STORE_SRC_ID, STORE_NAME, STORE_TYPE, HOUSE_NUMBER, CITY_NAME, ZIP_CODE, COUNTY_NAME, TA_INSERT_DT, TA_UPDATE_DT, TA_SOURCE_SYSTEM, TA_SOURCE_ENTITY)
SELECT
    NEXTVAL('BL_DM.SEQ_DIM_STORES'),
    s.STORE_ID::VARCHAR,
    COALESCE(s.STORE_NAME,   'n.a.'),
    COALESCE(s.STORE_TYPE,   'n.a.'),
    COALESCE(a.HOUSE_NUMBER, -1),
    COALESCE(cit.CITY_NAME,    'n.a.'),
    COALESCE(cit.ZIP_CODE,     'n.a.'),
    COALESCE(c.COUNTY_NAME, 'n.a.'),
    CURRENT_DATE,
    CURRENT_DATE,
    'BL_3NF',
    'CE_STORES'
    FROM BL_3NF.CE_STORES as s 
    LEFT JOIN BL_3NF.CE_ADDRESSES a ON s.ADDRESS_ID=a.ADDRESS_ID
	LEFT JOIN BL_3NF.CE_CITIES cit ON a.CITY_ID= cit.CITY_ID
	LEFT JOIN BL_3NF.CE_COUNTIES c ON cit.COUNTY_ID= c.COUNTY_ID
	WHERE s.STORE_ID <> -1
	AND NOT EXISTS (
	SELECT 1 FROM BL_DM.DIM_STORES t
	WHERE t.STORE_SRC_ID = s.STORE_ID::VARCHAR);
	
UPDATE BL_DM.DIM_STORES dm
SET
  STORE_NAME = COALESCE(s.STORE_NAME,   'n.a.'),
  STORE_TYPE =  COALESCE(s.STORE_TYPE,   'n.a.'),
  HOUSE_NUMBER =  COALESCE(a.HOUSE_NUMBER, -1),
  CITY_NAME =  COALESCE(cit.CITY_NAME,    'n.a.'),
  ZIP_CODE =  COALESCE(cit.ZIP_CODE,     'n.a.'),
  COUNTY_NAME = COALESCE(c.COUNTY_NAME, 'n.a.'),
  TA_UPDATE_DT = CURRENT_DATE
 FROM BL_3NF.CE_STORES AS s
    LEFT JOIN BL_3NF.CE_ADDRESSES a ON s.ADDRESS_ID=a.ADDRESS_ID
	LEFT JOIN BL_3NF.CE_CITIES cit ON a.CITY_ID= cit.CITY_ID
	LEFT JOIN BL_3NF.CE_COUNTIES c ON cit.COUNTY_ID= c.COUNTY_ID
WHERE dm.STORE_SRC_ID = s.STORE_ID::VARCHAR
AND s.STORE_ID <> -1
 AND (
      dm.STORE_NAME   IS DISTINCT FROM COALESCE(s.STORE_NAME,   'n.a.') OR
      dm.STORE_TYPE   IS DISTINCT FROM COALESCE(s.STORE_TYPE,   'n.a.') OR
      dm.HOUSE_NUMBER IS DISTINCT FROM COALESCE(a.HOUSE_NUMBER, -1)     OR
      dm.CITY_NAME    IS DISTINCT FROM COALESCE(cit.CITY_NAME,    'n.a.') OR
      dm.ZIP_CODE     IS DISTINCT FROM COALESCE(cit.ZIP_CODE,     'n.a.') OR
      dm.COUNTY_NAME  IS DISTINCT FROM COALESCE(c.COUNTY_NAME, 'n.a.'));

