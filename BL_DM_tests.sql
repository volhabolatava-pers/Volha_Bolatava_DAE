SELECT 'STORES' AS entity, 
       (SELECT count(*) FROM BL_3NF.CE_STORES WHERE STORE_ID <> -1) AS src_cnt,
       (SELECT count(*) FROM BL_DM.DIM_STORES WHERE STORE_SURR_ID <> -1) AS dm_cnt,
       (SELECT count(*) FROM BL_3NF.CE_STORES WHERE STORE_ID <> -1) - 
       (SELECT count(*) FROM BL_DM.DIM_STORES WHERE STORE_SURR_ID <> -1) AS diff

UNION ALL

